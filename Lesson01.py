import turtle as t

t.home()
t.penup()
t.position()
t.heading()
t.pendown()
t.pencolor('red')
t.fillcolor('red')
t.begin_fill()
t.circle(20)
t.end_fill()
t.home()

t.goto(50,-40)
t.left(90)
t.pencolor('yellow')
t.fillcolor('yellow')
t.begin_fill()
t.circle(50,180)
t.end_fill()
t.pencolor('brown')
t.fillcolor('brown')
t.begin_fill()
t.left(30)
t.forward(100)
t.left(120)
t.forward(100)
t.end_fill()
t.penup()
t.home()
t.hideturtle()

t.mainloop()